package day01;

public class VariableEx {
	public static void main(String[] args) {
		int num = 10;
		System.out.println(num);
		
		int num2 = 20;
		System.out.println(num2);
		
		num2 = 30;
		System.out.println(num2);
		
		int result = num + num2 + 10;
		System.out.println(result);
		
		String str = "2";
		System.out.println(str);
	}
}
